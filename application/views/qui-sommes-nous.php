    <h2>Qui sommes-nous ?</h2>
    <p>Dans le cadre de la filière Art-Luxe et Création Lorraine, le dispositif des "pôles de compétences" initié par la Région Lorraine répond à son ambition de conforter des filières synonymes d'identité et de développement.</p>

    <p>L'objectif du Pôle de Compétences est de renforcer la synergie entre les acteurs, de développer les compétences et qualifications identifiées au sein des filières, de veiller, stimuler et intégrer l'innovation et la recherche aux processus de formations.</p>

    <p>La démarche collective est privilégiée pour afficher une offre renforcée, cohérente et complète associant les écoles d'art, d'architecture, d'enseignement technique mais aussi les centres de valorisation, les manufactures, les TPE, et les professionnels quel que soit leur statut.</p>

    <p>Plusieurs axes de travail ont été établis :</p>
    <ul>
        <li>développement des métiers, des compétences et des formations</li>
        <li>innovation / création / design</li>
        <li>promotion de l'offre de formation</li>
        <li>plan de valorisation des métiers et savoir-faire des métiers d'art en Lorraine</li>
        <li>programme d'accompagnement à la création d'atelier auprès d'artistes (modèle économique, hébergement juridique et physique)</li>
    </ul>

    <p>La Région a choisi d'adosser le Pôle de Compétences au Cerfav, structure de référence en France réunissant centre de formation professionnelle continue, centre de formation par apprentissage et Pôle national d'innovation.</p>

