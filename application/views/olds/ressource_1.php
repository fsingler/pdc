    <h4>CAP : Définition</h4>
    
    <p>Le CAP est un diplôme national qui atteste d'un premier niveau de qualification professionnelle. Il est classé au niveau V de la nomenclature interministérielle des niveaux de formation.<br />
        Le titulaire d'un CAP est capable de s'intégrer dans une manufacture ou un atelier. Il peut créer ou reprendre une activité.</p>
    <p><strong>Période de formation en milieu professionnel - CAP Arts du verre et du cristal, CAP Arts et techniques du verre</strong></p>
